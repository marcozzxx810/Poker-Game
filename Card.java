/**
 *  Card Class
 *  having the rank and suit
 *
 * @author MAK CHAK WING
 * @version Nov 17, 2019
 */

public class Card  {
    public int rank;
    public int suit;

    /**
     * Card constructor and initialize the card with rank and suit
     * @param rank the rank of the card
     * @param suit the suit of the card
     */

    public Card(int rank, int suit) {
        this.rank = rank;
        this.suit = suit;
    }


}
